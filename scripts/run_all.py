def export_complex_tables(complex_, sample, c, out_data):
    pd.DataFrame({
        "cell_id": np.arange(complex_.n_cells), "x": complex_.coords[:,0], "y": complex_.coords[:,1],
        "source": complex_.source_mask.astype(int), "degree": complex_.degree,
        "stroma": sample["stroma"], "necrosis": sample["necrosis"],
        "endothelial": sample["endothelial"], "concentration_true": c,
    }).to_csv(out_data / "cells.csv", index=False)
    u,v=complex_.edges[:,0],complex_.edges[:,1]
    pd.DataFrame({
        "edge_id":np.arange(complex_.n_edges),"tail_cell":u,"head_cell":v,
        "barrier":sample["barrier"],"conductance_true":sample["conductance"],
    }).to_csv(out_data / "interfaces.csv", index=False)
    np.savez_compressed(out_data / "incidence_and_hodge.npz", D=complex_.D.toarray(),
                        H=complex_.edge_hodge(), mask=complex_.mask, coords=complex_.coords,
                        edges=complex_.edges, source_mask=complex_.source_mask)


def main():
    parser=argparse.ArgumentParser()
    parser.add_argument("--config",default=str(ROOT/"configs/default.yaml"))
    args=parser.parse_args()
    cfg=yaml.safe_load(Path(args.config).read_text())
    out_data=ROOT/"data/demo"; out_fig=ROOT/"figures"; out_models=ROOT/"results/models"
    for p in (out_data,out_fig,out_models,ROOT/"results"): p.mkdir(parents=True,exist_ok=True)
    set_seed(int(cfg["seed"]))
    complex_=CubicalTumourComplex.circular(int(cfg["grid_size"]))
    total=int(cfg["n_train"])+int(cfg["n_val"])+int(cfg["n_test"])
    seeds=[int(cfg["seed"])+i*17 for i in range(total)]
    samples,x,g,c=stack_samples(complex_,seeds,float(cfg["reaction_rate"]),float(cfg["dirichlet_penalty"]))
    nt=int(cfg["n_train"]); nv=int(cfg["n_val"])
    train=slice(0,nt); val=slice(nt,nt+nv); test=slice(nt+nv,total)
    D_t=torch.tensor(complex_.D.toarray(),dtype=torch.float32)
    H=complex_.edge_hodge(); H_t=torch.tensor(H,dtype=torch.float32)
    src_t=torch.tensor(complex_.source_mask.astype(np.float32))
    in_dim=x.shape[-1]
    local=PositiveEdgeMLP(in_dim,int(cfg["hidden_dim"]))
    hodge=HodgeEdgeNet(in_dim,int(cfg["hidden_dim"]),int(cfg["hodge_layers"]))
    local,hist_l=train_model(local,H_t,D_t,src_t,x[train],g[train],c[train],x[val],g[val],c[val],cfg,False)
    hodge,hist_h=train_model(hodge,H_t,D_t,src_t,x[train],g[train],c[train],x[val],g[val],c[val],cfg,True)
    torch.save(local.state_dict(),out_models/"local_edge_mlp.pt")
    torch.save(hodge.state_dict(),out_models/"cmc_hodgenet.pt")
    homogeneous=float(np.exp(np.mean(np.log(g[train]))))
    metrics,pred=evaluate_models(complex_,samples[nt+nv:],x[test],g[test],c[test],local,hodge,H_t,cfg,homogeneous)
    metrics.to_csv(ROOT/"results/test_metrics_by_sample.csv",index=False)
    aggregations={col:["mean","std"] for col in ["concentration_rmse","concentration_relative_l2",
                    "penetration_fraction_error","conductance_log_rmse","barrier_auc","conservation_residual_linf"]}
    summary=metrics.groupby("model",sort=False).agg(aggregations)
    summary.columns=[f"{a}_{b}" for a,b in summary.columns]
    summary=summary.reset_index(); summary.to_csv(ROOT/"results/summary_metrics.csv",index=False)
    hist_l["model"]="Local edge MLP"; hist_h["model"]="CMC-HodgeNet"
    pd.concat([hist_l,hist_h]).to_csv(ROOT/"results/training_history.csv",index=False)
    analytical=analytical_benchmark(out_data,out_fig)
    plot_phantom(complex_,samples[nt+nv],out_fig); plot_complex(complex_,samples[nt+nv],out_fig)
    plot_transport(complex_,0,pred,c[test],out_fig); plot_metrics(summary,out_fig)
    plot_training(hist_l,hist_h,out_fig); plot_workflow(out_fig); plot_experimental_design(out_fig)
    export_complex_tables(complex_,samples[nt+nv],c[test][0],out_data)
    # Long-form predictions for exact reproducibility.
    records=[]
    for name,(gs,cs) in pred.items():
        for si in range(gs.shape[0]):
            for cid,val in enumerate(cs[si]): records.append({"model":name,"sample":si,"cell_id":cid,"concentration":val})
    pd.DataFrame(records).to_csv(ROOT/"results/test_concentrations.csv",index=False)
    run_meta={"configuration":cfg,"n_cells":complex_.n_cells,"n_interfaces":complex_.n_edges,
              "homogeneous_conductance":homogeneous,"software":{"numpy":np.__version__,"torch":torch.__version__}}
